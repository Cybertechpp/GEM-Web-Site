     <h1 class="header center green-text">GEM Headquarters</h1>        
    
<h4>Overview</h4>
        <p>In 2012 Circle of Children was donated a fully-developed Conference & Retreat Center at Triangle Lake in Oregon. This center is nestled on 60 acres of land in the coastal mountains and rainforest of western Oregon, and is surrounded by 180 acres of private wildlife preserve. The couple who donated this land expressed it as being a gift for the children of the earth, in the loving hands of Circle of Children.</p>

        <p>We are pleased to announce that the Triangle Lake Center is evolving into the headquarters for the GEM Global Network.</p>

        <p>GEM's Headquarters will become a thriving hub for showcasing sustainable models of living and learning. We will be offering opportunities for groups and organizations that are influential in the spheres of ecological stewardship, sustainable design, renewable energy, holistic health, alternative education, creative arts, community building, peacemaking and youth empowerment to host their upcoming convergences, retreats and events. The center will become a demonstration site for harmonious living, and it will be sustained by the voluntary contributions of the whole community.</p>

        <p>GEM Headquarters will also be a space for GEM Youth to plan and host events that express their creative solutions for change. Soon come, interested youth will be able to apply for this opportunity by submitting a project or event proposal.</p>

        <p>We are especially interested in welcoming groups and organizations that would like to integrate GEM Youth & Projects into their existing conferences or retreats. We see this interaction as an opportunity to enrich the experience of both the youth and the organization by creating a symbiotic relationship that will support the event, inspire participants and create a holistic learning environment for the next generation.</p>

        <h4>Facility Features:</h4>
<p>The facility features a commercial-sized kitchen, dining hall, large activity barn with a theater stage, two lodges overlooking Little Lake, cabins, offices and Triangle Lake access with a private dock.</p>

        <h4>Accessibility:</h4>
        <p>Triangle Lake Conference & Retreat Center is located 45 minutes from Eugene and just over 2 hours from Portland, Oregon.</p>

    
        
